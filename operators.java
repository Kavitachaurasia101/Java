/* Arithmetic Operators:-
1. Addition(+)
2. Subtraction(-)
3. Multiplication(*)
4. Division(/)
5. Modulus(%)
 */
public class operators{
    public static void main(String[] args){
        int a = 15;
        int b = 10;
        int sum = a+b;
        int subtract = a-b;
        int multiply = a*b;
        int divide = a/b;
        int modulus = a%b;
        System.out.println("The value of a and b after sum is: " + sum);
        System.out.println("The value of a and b after subtraction is: " + subtract);
        System.out.println("The value of a and b after multiplication is: " + multiply);
        System.out.println("The value of a and b after division is: " + divide);
        System.out.println("The value of a and b rimainder is: " + modulus);
    }
}